/**
 * @Description gateway - 启动类
 * @Author spw
 * @Date 2021/5/22
 */
@EnableEurekaClient
@SpringBootApplication
public class GatewayApplication {
    public static void main(String[] args) {
        SpringApplication.run(GatewayApplication.class, args);
    }
}
